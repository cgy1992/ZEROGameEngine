using namespace ZERO;
using namespace ZERO::SCENE;

ShaderNode::ShaderNode()
	: SceneNode()
	, mpVertexShader(nullptr)
	, mpPixelShader(nullptr)
{
	// Empty
}

//----------------------------------------------------------------------------------------------------

ShaderNode::~ShaderNode()
{
	// Empty
}

//----------------------------------------------------------------------------------------------------

void ShaderNode::OnRender()
{
	if (mpVertexShader != nullptr)
	{
		mpVertexShader->Bind();
	}
	if (mpPixelShader != nullptr)
	{
		mpPixelShader->Bind();
	}
}